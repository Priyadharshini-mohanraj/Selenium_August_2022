package week1.day1;

public class ConditionalStatements {
public static void main(String[] args) {
	int a = 50;
	System.out.println(a>10);
	if(a>10) {
		// true block
		System.out.println("A is greater than 10");
	} else {
		// false block
		System.out.println("A is lesser than 10");

	}
	
}
}
